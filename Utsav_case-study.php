<p>The store entered into the online world in 2003 with the exclusive collection sourced from the remotest corners of the country. Customer satisfaction and their delight is the center of every activity at Utsav Fashion and this is what brought them in contact with Round Ark. The brand had the social presence but was struggling to engage the visitors and increase the follower base. They understood the immense power of social media in increasing their business with more customers from the social media. But the problem was a right direction and effective strategies to engage the audience. 

</p>
<p>The social media team at Round Ark was tasked to create a customized strategy to grow Utsav Fashion's online following on different social media platforms including Facebook and Twitter with increased interactions with the existing and potential customers. 

</p>
