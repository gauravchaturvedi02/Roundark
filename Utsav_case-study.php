    <p><strong>
        GETTING RESULT:
        </strong></p>
<p><strong>Facebook Competition:</strong><br/>
The main focus, in the beginning, was to increase the engagement and get more followers. To increase the number of followers, comments likes and re-tweets we ran different competitions. The campaigns were a huge success with an increase of 20, 454 fans in a short span of just three months. 
	
</p>
<p>	
The success of the competitions encouraged us to move onto our other social media marketing strategies for all the three channels: Facebook, Twitter and Google Plus. That included sharing informative posts and different contests like MixNMatch, Hunt for Earrings, The Saree Twist, and Bollywood Focus Post. The main objective behind all these contests was to grow website traffic, which we achieved successfully. 
</p><p>
Our efforts paid them in a long run with a continuous increase in the number of followers and fans on different social media channels. 

</p>
