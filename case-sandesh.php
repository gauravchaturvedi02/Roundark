<p><strong>Research:</strong><br/>
Working on any project is impossible until we have in-depth knowledge about it. And gaining this knowledge is possible only with a thorough research. The major challenge while working on SEO for Sandesh was to find the bad links and fix them. 

<br/>The SEO experts analyzed the non-crawled pages to fix problems like 404, redirection and image optimization. They also performed competitor analysis to gain some knowledge and use helpful strategies. 

</p>
<p><strong>Our SEO Approach:
</strong></p>
<p>We worked on every aspect to ensure lasting results. The strategies include backlink analysis to segregate the bad links and remove them. Content is the backbone for a media house and keeping this thing in the mind we tweaked their website content as per the visitor behavior. This helped us control the bounce rate and increase the average time spent on the website. 

</p>
<p>   
The next action plan in the strategies was to create brand awareness. The regular readers were aware of the print edition, but very few knew about the online presence of Sandesh. So we planned to grow brand awareness with social media channels. We also worked with the development team to restructure the website for increased crawling rate. 
</p>
    

