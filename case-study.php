<p>Huge partnered with Gucci to optimize the international luxury brand's digital offering for mobile to boost sales and drive conversion on smartphones and tablets around the world</p>
<p>With a larger, overall redesign on the horizon, there were two big constraints on the mobile projectâ€”we could only use existing code and photography, and it had to be done in two months Within these limits, the design and user experience challenge was to maintain a premium feel while also streamlining the purchase path; browsing, checking out and sharing favorite products on social channels all needed to be smooth and intuitive</p>
<p>Applying learning from previous luxury e-commerce projects like Barneyscom, we made the products the heroes of every page, spotlighting big, high-resolution images and letting description and price fall below the fold  Knowing that Gucci customers (and luxury shoppers in general) tend to purchase only one expensive item at a time, we expedited the checkout process and condensed it into three simple steps  To customize the experience for global customers, we developed versions for multiple regions across Asia, Europe and the Americas, complying with the extensive region-specific rules governing online transactions (notably in Korea, Japan and Italy)  Finally, in order to facilitate repeat purchases, we made sure the mobile sites stored personal and billing information  We delivered all this for iOS and Android devices within the two-month window </p>
